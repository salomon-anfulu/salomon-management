lines = csv_data.strip().split('\n')
# 行0 = 空行, 行1 = 表头
header = lines[1].split(',')
# 日期列表: header[1]=6/1, header[N]=6/N
dates = {}
for i in range(1, len(header)):
    if header[i].strip():
        dates[i] = header[i].strip()  # e.g. "2026/6/24"

# 解析门迎排班行 (行2开始)
for target_day in ['6/24', '6/25', '6/26']:
    # 找到该日期对应的列索引
    target_col = None
    for col_idx, col_date in dates.items():
        if col_date.endswith(target_day):
            target_col = col_idx
            break
    if target_col is None:
        continue
    
    print(f"\n=== {target_day} 门迎排班 (列{target_col}) ===")
    slots = []
    for line in lines[2:]:
        cells = line.split(',')
        time_label = cells[0].strip()
        if not time_label or '-' not in time_label:
            continue
        # 该列的值
        if target_col < len(cells):
            val = cells[target_col].strip()
            if val:
                # 处理多人情况（如 "王雅澜/何秋烨"）
                for person in val.split('/'):
                    person = person.strip()
                    if person:
                        slots.append({'time': time_label, 'staff': person})
    
    for s in slots:
        print(f"  {s['time']} → {s['staff']}")
    print(f"共 {len(slots)} 条")
